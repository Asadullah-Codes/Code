#Most used string function

# | Function       | Meaning                                         | Example                    |
# | -------------- | ----------------------------------------------- | -------------------------- |
# | `str()`        | Value ko string me convert karta hai            | `str(25)`                  |
# | `len()`        | String ki length batata hai                     | `len("Hello")`             |
# | `upper()`      | String ko uppercase me convert karta hai        | `"hello".upper()`          |
# | `lower()`      | String ko lowercase me convert karta hai        | `"HELLO".lower()`          |
# | `strip()`      | Start aur end ke extra spaces remove karta hai  | `"  hello  ".strip()`      |
# | `replace()`    | Word ya character change karta hai              | `"Hello".replace("H","J")` |
# | `split()`      | String ko list me divide karta hai              | `"a,b,c".split(",")`       |
# | `find()`       | Character ki position find karta hai            | `"Hello".find("e")`        |
# | `count()`      | Character kitni dafa aaya count karta hai       | `"hello".count("l")`       |
# | `startswith()` | Check karta hai string kis se start ho rahi hai | `"Hello".startswith("H")`  |
